public class DemoLoops {
    
    public static void main(String [] a){

        int i = 1;
        while(i<=10){
            System.out.println(i);
            i++;
        }
        int j = 28;
        do{
            System.out.println(j);
            i++;
        }while(j<=10);
        for (int k = 0; k<=5; k++){
            System.out.println(k);
        }
    }
}
